        <p>“พล.อ.ประยุทธ์ คือบุคคลสาธารณะที่ชำรุดแล้วไม่สามารถใช้การได้อีกต่อไป ปรากฏชัดจากพรหมพินาศ 4 คือ เศรษฐกิจพังพินาศ หลอกลวง ประชาชน กว่า7 ปีมีแต่ความทุกข์ ประชาชนแตกแยก ขาดความรัก สามัคคีปรองดอง เอื้อเฟื้อต่อกันตามพระราชดำริ ทำให้บ้านเมืองจมปลักอยู่กับที่และถอยหลัง ใช้รัฐธรรมนูญที่เขียนโดยคนบาปเช่นนายมีชัย ฤชุพันธุ์ และพวก ซึ่งเคยร่างรัฐธรรมนูญฉบับปี 2534 จนทำให้ประเทศชาติพังพินาศ เกียรติภูมิของทหาร ซึ่งเป็นรั้วของชาติถูกลดศักดิ์ศรีมาแล้วครั้งหนึ่งจากเหตุการณ์พฤษภา’35 และรัฐธรรมนูญที่เขียนโดยคนบาป ปัจจุบันได้สร้างความเลวร้ายย่อยยับให้กับประเทศชาติและประชาชนยิ่งกว่าเดิม จึงเป็นหน้าที่ของประชาชนกลุ่มการเมืองพรรคการเมือง ส.ส. และส.ว จะต้องร่วมกันมีจิตสำนึกต่อแผ่นดินเกิด โดยเฉพาะ พล.อ.ประวิตร ที่มีโอกาสจะแก้ตัวเพื่อประเทศชาติและประชาชน นำให้เกิดการเปลี่ยนแปลงโดยเร็ว ไม่เช่นนั้น พล.อ.ประวิตร อาจต้องร่วมกันรับผิดชอบกับความเสียหายที่เกิดจากการกระทำของพล.อ.ประยุทธ์ ด้วย ” นายอดุลย์ กล่าว</p><br>

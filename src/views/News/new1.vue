    <h2>ประธานญาติวีรชนฯเรียกร้อง“ประยุทธ์” ลาออกเดี๋ยวนี้ไม่ต้องรอยุบสภาหลังเอเปกปลายปี เตือนยิ่งยื้อประเทศยิ่งเสียหาย เรียกร้อง”ประวิตร”วอนกลุ่มทุนใหญ่เลิกสนับสนุนระบอบประยุทธ์ก่อนประชาชนจะหมดความอดทน</h2><br>
    <h3><p>เมื่อวันที่ 20 มี.ค.นายอดุลย์ เขียวบริบูรณ์ ประธานคณะกรรมการญาติวีรชนพฤษภา 35 เปิดเผยถึงกรณีรัฐบาลวางไทมไลน์ยุบสภาหลังจบการประชุมเอเปกเดือนพ.ย. ว่า พล.อ.ประยุทธ์ จันทร์โอชา นายกรัฐมนตรี ไม่ต้องรอยุบสภาถึงปลายปีความจริงควรลาออกจากนายกฯนานแล้ว เพราะประชาชนเรียกร้องมาอย่างต่อเนื่องแต่กลับไม่ได้ยิน การที่นายกฯบอกว่าจะเป็นผู้ตัดสินใจยุบสภาในสถานการณ์ที่เหมาะสม นั้น ตนและประชาชนส่วนใหญ่เห็นว่า ช่วงเวลาปัจจุบันนี้แหละคือสถานการณ์ที่เหมาะสมสำหรับพล.อ.ประยุทธ์ที่จะลงจากอำนาจได้แล้ว ไม่ต้องรอไปถึงปลายปีเพื่อหวังจะสร้างเกียรติยศให้ตัวเองว่าเป็นนายกฯที่บรรดาผู้นำประเทศต่างๆ จะมายกย่องสรรเสริญเพราะเกียรติยศที่แท้จริง คือ การเสียสละลาออกเพื่อให้โอกาสประเทศเดินหน้าหลุดพ้นความทุกข์ยากที่ พล.อ.ประยุทธ์ และองคาพยพไม่สามารถแก้ปัญหาได้ต่างหากถึงจะมีคนทั้งประเทศปรบมือให้</p><br>
